    private void customizeDesign()
    {
        
        jLabel_Check.setVisible(false);

        String[] columnNames = new String[]
        {
            "STT", "Tên sách", "Mã sách", "Ngày mượn", "Ngày hẹn trả", "Ngày trả", "Trạng thái"
        };
        
        // Customize table
        populateTable(columnNames);
    }

    private void populateTable(String[] columnNames)
    {

        CustomTableModel<Borrows> model = new CustomTableModel<Borrows>(columnNames)
        {
            @Override
            public Object getValueAt(int rowIndex, int columnIndex)
            {
                Object temp = null;
                Borrows borrow = getDataObject(rowIndex);
                switch (columnIndex)
                {
                    case 0:
                        return temp = rowIndex + 1;
                    case 1:
                        return temp = borrow.getBookId().getBookTitleId().getTitle();
                    case 2:
                        return temp = borrow.getBookId().getCode();
                    case 3:
                        return temp = borrow.getBorrowDate();
                    case 4:
                        return temp = borrow.getPlanReturnDate();
                    case 5:
                        return temp = borrow.getReturnDate();
                    case 6:
                        if (borrow.getReturnDate() == null)
                        {
                            return temp = "Mượn sách";
                        }
                        else
                        {
                            return temp = "Trả sách";
                        }
                    default:
                        throw new ArrayIndexOutOfBoundsException(columnIndex);
                }
            }

        };

        jTable_BorrowBooks.setModel(model);
        jScrollPane1.getViewport().add(jTable_BorrowBooks);
    }

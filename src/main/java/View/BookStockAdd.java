    private void jButton_AddActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton_AddActionPerformed
    {//GEN-HEADEREND:event_jButton_AddActionPerformed
        // TODO add your handling code here:
        int n = (int) jSpinner_Number.getValue();
        
        for (int i = 0; i < n; i++)
        {
            Books book = new Books();
            
            book.setBookTitleId(BookTitlesJpaController.findBookTitles(bookTitleId));
            book.setCreatedDate(new Date());
            book.setStatus(1);
            
            BooksJpaController.create(book);
        }
        
        JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.OK_OPTION);
        
        this.dispose();
    }//GEN-LAST:event_jButton_AddActionPerformed


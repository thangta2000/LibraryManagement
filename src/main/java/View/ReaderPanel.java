    private void populateTable()
    {
        readers = new ArrayList<>(ReadersJpaController.findReadersEntities());
        var maxRow = (newHeight - this.getPreferredSize().height + jScrollPane1.getViewport().getPreferredSize().height) / 28;

        String[] columnName =
        {
            "No.", "Tên", "Số thẻ", "Quốc tịch", "Số CCCD", "Ngày sinh", "Giới tính", "Địa chỉ", "Điện thoại", "Nghề nghiệp", "Cơ quan", "Id"
        };

        // Create model booktitles by creating anonymous nest class of CustomTableModel<T>
        CustomTableModel<Readers> model = new CustomTableModel<Readers>(readers.size() <= maxRow
                ? readers : readers.subList(0, maxRow - 1), columnName)
        {
            @Override
            public Object getValueAt(int rowIndex, int columnIndex)
            {
                Object temp = null;
                Readers reader = getDataObject(rowIndex);
                switch (columnIndex)
                {
                    case 0:
                        return temp = rowIndex + 1;
                    case 1:
                        return temp = reader.getFullName();
                    case 2:
                        return temp = reader.getMemberCard();
                    case 3:
                        return temp = reader.getCountryId().getName();
                    case 4:
                        return temp = reader.getIdentityCard();
                    case 5:
                        return temp = reader.getBirthDay();
                    case 6:
                        return temp = reader.getGender() ? "Nam" : "Nữ";
                    case 7:
                        return temp = reader.getAddress();
                    case 8:
                        return temp = reader.getPhone();
                    case 9:
                        return temp = reader.getJobTitle();
                    case 10:
                        return temp = reader.getWorkPlace();
                    case 11:
                        return temp = reader.getId();
                    default:
                        throw new ArrayIndexOutOfBoundsException(columnIndex);
                }
            }

        };

        jTable1.setModel(model);

        jScrollPane1.getViewport().add(jTable1);
        customizeColumns();
        jPanelTable.setLayout(new BorderLayout());
        jPanelTable.add(jScrollPane1);
        jPanelTable.validate();

        jPanelTable.setVisible(true);
    }

    private void customizeColumns()
    {
        int remainWidth = newWidth - 30 - 100 - 100 - 100 - 60 - 100 - 100 - 100 - 120 - 0;
        int autoSize = Math.round(remainWidth * 0.55f);

        int[] columnWidth =
        {
            30, autoSize, remainWidth - autoSize, 100, 100, 100, 60, 100, 100, 100, 120, 0
        };

        //Design table column
        TableColumnModel columnModel = jTable1.getColumnModel();
        for (int i = 0; i < columnWidth.length; i++)
        {
            var column = columnModel.getColumn(i);
            column.setMinWidth(columnWidth[i]);
            column.setMaxWidth(columnWidth[i]);
            column.setPreferredWidth(columnWidth[i]);
        }
    }


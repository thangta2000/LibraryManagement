    private boolean validateInput(String... input)
    {
        boolean check = true;
        jLabel_ErrorName.setForeground(new java.awt.Color(214,217,223));
        jLabel_ErrorPassword.setForeground(new java.awt.Color(214,217,223));
        
        for (int i = 0; i < input.length; i++)
        {
            if (input[i] == null || input[i].trim().isEmpty())
            {
                check = false;
                if (i == 0)
                {
                    jLabel_ErrorName.setForeground(new java.awt.Color(226,94,86));
                }
                else
                {
                    jLabel_ErrorPassword.setForeground(new java.awt.Color(226,94,86));
                }
            }
        }
        
        return check;
    }

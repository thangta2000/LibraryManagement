    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        // TODO add your handling code here:
        
        if (validateInput(jTextField_Username.getText(), new String(jPasswordField_Pasword.getPassword())))
        {
            var info = controller.findLogin(jTextField_Username.getText(), new String(jPasswordField_Pasword.getPassword()));

            if (info != null && info.getId() > 0)
            {
                Constants.USERINFO = info;
                this.dispose();

                var frame = new Home();
                frame.setExtendedState(frame.getExtendedState() | JFrame.MAXIMIZED_BOTH);
                frame.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Không tìm thấy tài khoản!", "Thông báo", JOptionPane.OK_OPTION);
            }
        }
    }//GEN-LAST:event_jButton_LoginActionPerformed

